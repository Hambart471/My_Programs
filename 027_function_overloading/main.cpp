// Function Overloading

// Function overloading allows multiple functions with the same name but
// different signatures

// The compiler resolves the correct function at compile-time

// Useful for handling different data types with similar operations

#include <iostream>

int sum(int a)
{
	return a;
}

double sum(double a, double b)
{
	return a + b;
}

